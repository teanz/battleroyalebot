package Domain.Partybus;

public class Stats {

    private String id;

    private int games;

    private String platform;

    private int minutes;

    private String p;

    private int placeA;

    private int placeB;

    private int kills;

    private String m;

    private int placeC;

    private String modified;

    public Stats() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getGames() {
        return games;
    }

    public void setGames(int games) {
        this.games = games;
    }

    public String getPlatform() {
        return platform;
    }

    public void setPlatform(String platform) {
        this.platform = platform;
    }

    public int getMinutes() {
        return minutes;
    }

    public void setMinutes(int minutes) {
        this.minutes = minutes;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public int getPlaceA() {
        return placeA;
    }

    public void setPlaceA(int placeA) {
        this.placeA = placeA;
    }

    public int getPlaceB() {
        return placeB;
    }

    public void setPlaceB(int placeB) {
        this.placeB = placeB;
    }

    public int getKills() {
        return kills;
    }

    public void setKills(int kills) {
        this.kills = kills;
    }

    public String getM() {
        return m;
    }

    public void setM(String m) {
        this.m = m;
    }

    public int getPlaceC() {
        return placeC;
    }

    public void setPlaceC(int placeC) {
        this.placeC = placeC;
    }

    public String getModified() {
        return modified;
    }

    public void setModified(String modified) {
        this.modified = modified;
    }
}
